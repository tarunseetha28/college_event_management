public ArrayList<User> userList(){
        ArrayList<User> usersList = new ArrayList<>();
         try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=
            DriverManager.getConnection("jdbc:mysql://localhost:3306/event_mgmt_sys?useSSL=false","root","12345"); 
            String a=jLabel3.getText();    
            
            String query1="SELECT * FROM event where organiser='"+a+"'";
            Statement st= con.createStatement();
            ResultSet rs= st.executeQuery(query1);
            User user;
            while(rs.next()){
                user=new User(rs.getInt("event_id"), rs.getString("event_name"), rs.getString("event_date"), rs.getInt("seats"), rs.getString("organiser"),rs.getInt("amount"));
                usersList.add(user);
            }
         }
         catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
         return usersList;
    }
